export const getVehiclesByZipCode = (zipCode: string): Vehicle[] => {
  return mockVehicles.filter((vehicle) => vehicle.zipCode === zipCode);
};

export const getAllVehicles = (): Vehicle[] => {
  return mockVehicles;
};

export const getUniqueMakes = (vehicles: Vehicle[]): string[] => {
  const makes = vehicles.map((vehicle) => vehicle.make);
  return Array.from(new Set(makes)).sort();
};

export const getUniqueColors = (vehicles: Vehicle[]): string[] => {
  const colors = vehicles.map((vehicle) => vehicle.color);
  return Array.from(new Set(colors)).sort();
};
